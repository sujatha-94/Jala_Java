package Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int p = 8;
        int q = 12;

        System.out.println("p < q: " + (p < q));
        System.out.println("p <= q: " + (p <= q));
        System.out.println("p > q: " + (p > q));
        System.out.println("p >= q: " + (p >= q));
    }
}
